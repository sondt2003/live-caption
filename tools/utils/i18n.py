class I18n:
    def __init__(self, language='vi'):
        self.language = language

    def get(self, key):
        return TRANSLATIONS.get(self.language, TRANSLATIONS['zh']).get(key, key)

    def set_language(self, language):
        if language in TRANSLATIONS:
            self.language = language

# Singleton
